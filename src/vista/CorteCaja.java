    private void mostrarTabla(){
        modelo mod=new modelo();
        String estado=null;
        String integrado=null;
        int caja=0;
        int ganancias=0;
        int netas=0;
        int desc=0;
        DefaultTableModel modelo;
        Statement st = null;
        ResultSet rs = null;
        String[] cabecera = {"Id","No.","Cliente","Total","Desc.","Fecha","Estado","Corte"};
        String[] registros = new String[8];
        String sql = "SELECT ticket.idticket,ticket.numero,ticket.cliente,ticket.total,ticket.porcentaje,ticket.tiempo,ticket.inventario,ticket.cantidad,"
                + "cliente.nombre as nombre, ticket.estado,ticket.integrado FROM ticket "
                + "INNER JOIN cliente on cliente.idcliente=ticket.cliente WHERE integrado='NO' GROUP BY numero ORDER BY idticket DESC";
        //establecemos los anchos en pixeles de las columnas
        int[] anchos = {0, 30,100,50,50,100,50,50};
        
        modelo = new DefaultTableModel(null, cabecera);
        Conexion con=new Conexion();
        Connection cn = con.conectar();
        try {
            st = cn.createStatement();
            rs = st.executeQuery(sql);
            while (rs.next()) {
                estado=rs.getString("estado");
                integrado=rs.getString("integrado");
                
                registros[0] = rs.getString("idticket");
                registros[1] = rs.getString("numero");
                registros[2] = rs.getString("nombre");
                registros[3] = rs.getString("total");
                registros[4] = rs.getString("porcentaje");
                registros[5] = rs.getString("tiempo");
                registros[6] = estado;
                registros[7] = integrado;
                modelo.addRow(registros);
                
                if (integrado.equals("NO") && !estado.equals("CANCELADO")){
                    caja+=rs.getInt("total");
                    
                }
                if (!estado.equals("CANCELADO")){
                ganancias+=rs.getInt("total");
                desc+=rs.getInt("porcentaje");
                netas+=mod.ganancia(rs.getInt("inventario"), rs.getInt("cantidad"));
                }
            }
            tabla.setModel(modelo);
            for (int i = 0; i < cabecera.length; i++) {
                tabla.getColumnModel().getColumn(i).setPreferredWidth(anchos[i]);
                tabla.setFont(new java.awt.Font("Tahoma", 0, 10));
            }
            tabla.getColumnModel().getColumn(0).setMaxWidth(0);
            tabla.getColumnModel().getColumn(0).setMinWidth(0);
            tabla.getColumnModel().getColumn(0).setPreferredWidth(0);
            
            txtCaja.setText(String.valueOf(caja));
            txtGanancias.setText(String.valueOf(ganancias));
            txtNetas.setText(String.valueOf(netas));
            txtDesc.setText(String.valueOf(desc));
           
        } catch (SQLException ex) {
            System.out.println("Sin poder ejecutar el query a la tabla" + ex);
        } finally {
            try {
                cn.close();
            } catch (SQLException ex) {
                Logger.getLogger(CorteCaja.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }

