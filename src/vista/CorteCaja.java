    private void btnReimprimirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReimprimirActionPerformed
        int fila = tabla.getSelectedRow();
        String ticket = tabla.getValueAt(fila, 1).toString();
        String cliente = tabla.getValueAt(fila, 2).toString();
        if (fila >= 0) {
        int id =Integer.valueOf(ticket);
        
        modelo mod=new modelo();
        mod.impresionTicket(id, cliente, 0.0);
        } else {
            JOptionPane.showMessageDialog(this, "Selecciona un elemento de la tabla");
        }
    }//GEN-LAST:event_btnReimprimirActionPerformed

    private void btnCanceladoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCanceladoActionPerformed
        int fila = tabla.getSelectedRow();
        String estado = tabla.getValueAt(fila, 7).toString();
        System.out.println(estado);
        if (estado.equals("NO")){
        if (fila >= 0) {
            Conexion con=new Conexion(); 
            Connection cn = con.conectar();
            
            try {
                Statement st = cn.createStatement();
                String id = tabla.getValueAt(fila, 0).toString();
                String sql = "UPDATE ticket set estado='CANCELADO' WHERE idticket=" + id;
                st.execute(sql);
                
                mostrarTabla();
            } catch (SQLException ex) {
                System.out.println(ex);
            } 
        } else {
            JOptionPane.showMessageDialog(this, "Selecciona un elemento de la tabla");
        }
        }else{
           JOptionPane.showMessageDialog(this, "No puedes cancelar un ticket ya integrado"); 
        }
    }//GEN-LAST:event_btnCanceladoActionPerformed

