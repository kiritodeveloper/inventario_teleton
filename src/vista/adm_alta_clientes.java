        int fila = tabla.getSelectedRow();

        if (fila >= 0) {
            String id = tabla.getValueAt(fila, 0).toString();
            
                Connection cn = con.conectar();
                ResultSet rs = null;
                Statement st = null;
                try {
                    String activo = null;
                    st = cn.createStatement();
                    rs = st.executeQuery("SELECT activo FROM cliente WHERE idcliente='" + id + "'");
                    while (rs.next()) {
                        activo = rs.getString("activo");
                    }
                    if (activo.equals("SI")) {
                        st = cn.createStatement();
                        st.executeUpdate("UPDATE cliente SET activo='NO' WHERE idcliente='" + id + "'");
                    } else {
                        st = cn.createStatement();
                        st.executeUpdate("UPDATE cliente SET activo='SI' WHERE idcliente='" + id + "'");
                    }
                } catch (SQLException ex) {
                    Logger.getLogger(adm_alta_clientes.class.getName()).log(Level.SEVERE, null, ex);
                } finally {
                    DbUtils.closeQuietly(st);
                    DbUtils.closeQuietly(rs);
                    DbUtils.closeQuietly(cn);
                }

            
        } else {
            JOptionPane.showMessageDialog(this, "Selecciona un proveedor de la tabla");
        }
        tabla();
